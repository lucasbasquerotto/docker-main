SECONDS=0
DELAY=15
LIMIT=60
v=0

function print() {
  echo "========================================================================================================================================================"
  echo "$(date '+%Y-%m-%d %H:%M:%S')"
  echo "--------------------------------------------------------------------------------------------------------------------------------------------------------"
  docker stats --no-stream
  echo "--------------------------------------------------------------------------------------------------------------------------------------------------------"
  echo "$(date '+%Y-%m-%d %H:%M:%S')"
  echo "========================================================================================================================================================"
}

while [ $(($v + $DELAY)) -lt $LIMIT ]; 
do 
  print
  sleep $DELAY
  v=$(($v + $SECONDS))
done
